/*
n的阶乘
*/

int main()
{
    int n, i;
    long int factorial = 1;

    printf("请输入一个正数: ");
    scanf("%d",&n);

    // 判断输入的n是否为负数
    if (n < 0)
        printf("输入的n应该为正数！");

    else
    {
        for(i=1; i<=n; ++i)
        {
            factorial *= i;              // factorial = factorial*i;
        }
        printf("%d的阶乘为%l", n, factorial);
    }

    return 0;
}

