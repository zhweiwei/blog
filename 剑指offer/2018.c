void swap(int *xp, int *yp) //按址传递交换两个数
{ 
    int temp = *xp; 
    *xp = *yp; 
    *yp = temp; 
} 
  
 
void bubbleSort(int arr[], int n)  //
{ 
   int i, j; 
   for (i = 0; i < n-1; i++)       //冒泡排序每次将最大的数放到排序后的位置
  
          
       for (j = 0; j < n-i-1; j++)  //由于下面是j+1，所以j < n-i-1
           if (arr[j] > arr[j+1]) 
              swap(&arr[j], &arr[j+1]); 
} 

